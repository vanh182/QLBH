    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtTimSP, sb, "Mã sản phẩm không được để trống.");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        try {
            SanPhamDao dao = new SanPhamDao();
            SanPham n = new SanPham();
            n = dao.FindByID(txtTimSP.getText());
            if (n != null) {
                tblModel.setRowCount(0);
                tblModel.addRow(new Object[]{
                    n.getMaSP(), n.getTenSP(), n.getDVT(), n.getNuocSX(),
                    n.getGia()});
                tblModel.fireTableDataChanged();
                txtTimSP.setText("");
            } else {
                MessageDialogHelper.showErrorDialog(parentForm, "Không tìm thấy!", "Thông báo!");
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi!");
        }
    }//GEN-LAST:event_btnTimActionPerformed

    private void btnTHEMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTHEMActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMASP, sb, "Mã sản phẩm không được để trống.");
        DataValidator.validateEmpty(txtTENSP, sb, "Tên sản phẩm không được để trống.");
        DataValidator.validateEmpty(txtDVT, sb, "Đơn vị tính không được để trống.");
        DataValidator.validateEmpty(txtNUOCSX, sb, "Nước sản xuất không được để trống.");
        DataValidator.validateEmpty(txtGIA, sb, "Giá không được để trống.");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }

        SanPham n = new SanPham();
        n.setMaSP(txtMASP.getText());
        n.setTenSP(txtTENSP.getText());
        n.setDVT(txtDVT.getText());
        n.setNuocSX(txtNUOCSX.getText());
        n.setGia(Long.parseLong(txtGIA.getText()));

        if (new SanPhamDao().Insert(n)) {
            MessageDialogHelper.showMessageDialog(parentForm, "Sản phẩm đã được thêm!", "Thông báo!");
            loadData();
            list.add(n); // them vao danh sach SP
        } else {
            MessageDialogHelper.showErrorDialog(parentForm, "Thêm sản phẩm không thành công!", "Thông báo!");
        }
    }//GEN-LAST:event_btnTHEMActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMASP, sb, "Mã sản phẩm không được để trống.");
        DataValidator.validateEmpty(txtTENSP, sb, "Tên sản phẩm không được để trống.");
        DataValidator.validateEmpty(txtGIA, sb, "Giá không được để trống.");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }

        if (MessageDialogHelper.showConfirmDialog(parentForm, "Bạn có muốn cập nhật sản phẩm không?",
                "Hỏi") == JOptionPane.YES_OPTION) {
            SanPham n = new SanPham();
            n.setMaSP(txtMASP.getText());
            n.setTenSP(txtTENSP.getText());
            n.setDVT(txtDVT.getText());
            n.setNuocSX(txtNUOCSX.getText());
            n.setGia(Long.parseLong(txtGIA.getText()));

            if (new SanPhamDao().Update(n)) {
                MessageDialogHelper.showMessageDialog(parentForm, "Sản phẩm đã được cập nhật!", "Thông báo!");
                loadData();
            } else {
                MessageDialogHelper.showMessageDialog(parentForm, "Cập nhật sản phẩm không thành công do lỗi!", "Thông báo!");
            }
        }


    }//GEN-LAST:event_btnCapNhatActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMASP, sb, "Mã sản phẩm không được để trống.");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }

        if (MessageDialogHelper.showConfirmDialog(parentForm, "Bạn có muốn xoá sản phẩm không?",
                "Hỏi") == JOptionPane.YES_OPTION) {
            SanPhamDao dao = new SanPhamDao();
            if (dao.Delete(txtMASP.getText())) {
                MessageDialogHelper.showMessageDialog(parentForm, "Sản phẩm đã được xóa!", "Thông báo!");
                loadData();
                btnLamMoiActionPerformed(evt);
            } else {
                MessageDialogHelper.showMessageDialog(parentForm, "Xóa không thành công do lỗi!", "Thông báo!");
            }
        }


    }//GEN-LAST:event_btnXoaActionPerformed

