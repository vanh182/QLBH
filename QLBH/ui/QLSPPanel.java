    private void tblSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSPMouseClicked
        // TODO add your handling code here:
        try {
            int row = tblSP.getSelectedRow();
            if (row >= 0) {
                String id = (String) tblSP.getValueAt(row, 0);
                SanPhamDao dao = new SanPhamDao();
                SanPham nv = dao.FindByID(id);

                if (nv != null) {
                    txtMASP.setText(nv.getMaSP());
                    txtTENSP.setText(nv.getTenSP());
                    txtDVT.setText(nv.getDVT());
                    txtNUOCSX.setText(nv.getNuocSX());
                    txtGIA.setText(String.valueOf(nv.getGia()));

                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.toString(), "Lỗi");
        }
    }//GEN-LAST:event_tblSPMouseClicked

