    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMANV1, sb, "Mã nhân viên không được để trống.");
        DataValidator.validateEmpty(txtHOTEN1, sb, "Họ tên nhân viên không được để trống.");
        DataValidator.validateEmpty(txtSODT1, sb, "Số điện thoại không được để trống.");
        DataValidator.validateEmpty(txtNGVL1, sb, "Ngày vào làm không được để trống.");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }

        if (MessageDialogHelper.showConfirmDialog(parentForm, "Bạn có muốn cập nhật nhân viên không?",
                "Hỏi") == JOptionPane.YES_OPTION) {

            NhanVien n = new NhanVien();
            n.setMANV(txtMANV1.getText());
            n.setHOTEN(txtHOTEN1.getText());
            n.setSODT(txtSODT1.getText());
            n.setNGVL(txtNGVL1.getText());

            NhanVienDao dao = new NhanVienDao();
            if (dao.Update(n)) {
                MessageDialogHelper.showMessageDialog(parentForm, "Nhân viên đã được cập nhật!", "Thông báo!");
                loadData();
            } else {
                MessageDialogHelper.showMessageDialog(parentForm, "Cập nhật không thành công do lỗi!", "Thông báo!");
            }
        }

    }//GEN-LAST:event_btnCapNhatActionPerformed

