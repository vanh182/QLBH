    private void btnTimNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimNVActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtTimMaNV, sb, "Mã nhân viên không được để trống.");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        try {
            NhanVienDao dao = new NhanVienDao();
            NhanVien nv = new NhanVien();
            nv = dao.FindByID(txtTimMaNV.getText());
            if (nv != null) {
                tblModel.setRowCount(0);
                tblModel.addRow(new Object[]{
                    nv.getMANV(), nv.getHOTEN(), nv.getSODT(), nv.getNGVL()});
                tblModel.fireTableDataChanged();
                txtTimMaNV.setText("");
            } else {
                MessageDialogHelper.showErrorDialog(parentForm, "Không tìm thấy!", "Thông báo!");
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi!");
        }
    }//GEN-LAST:event_btnTimNVActionPerformed

