    private void tblNVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNVMouseClicked
        // TODO add your handling code here:
        try {
            int row = tblNV.getSelectedRow();
            if (row >= 0) {
                String id = (String) tblNV.getValueAt(row, 0);
                NhanVienDao dao = new NhanVienDao();
                NhanVien nv = dao.FindByID(id);
                if (nv != null) {
                    txtMANV1.setText(nv.getMANV());
                    txtHOTEN1.setText(nv.getHOTEN());
                    txtSODT1.setText(nv.getSODT());
                    txtNGVL1.setText(nv.getNGVL());
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.toString(), "Lỗi");
        }
    }//GEN-LAST:event_tblNVMouseClicked

