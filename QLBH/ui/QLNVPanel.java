    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMANV1, sb, "Mã nhân viên không được để trống.");
        DataValidator.validateEmpty(txtHOTEN1, sb, "Họ tên nhân viên không được để trống.");
        DataValidator.validateEmpty(txtSODT1, sb, "Số điện thoại không được để trống.");
        DataValidator.validateEmpty(txtNGVL1, sb, "Ngày vào làm không được để trống.");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }

        NhanVien n = new NhanVien();
        n.setMANV(txtMANV1.getText());
        n.setHOTEN(txtHOTEN1.getText());
        n.setSODT(txtSODT1.getText());
        n.setNGVL(txtNGVL1.getText());

        if (new NhanVienDao().Insert(n)) {
            MessageDialogHelper.showMessageDialog(parentForm, "Nhân viên đã được thêm!", "Thông báo!");
            loadData();
            list.add(n); // them vao danh sach NV
        } else {
            MessageDialogHelper.showErrorDialog(parentForm, "Thêm không thành công!", "Thông báo!");
        }
    }//GEN-LAST:event_btnThemActionPerformed

