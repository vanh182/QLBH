    private void tblKHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKHMouseClicked
        try {
            int row = tblKH.getSelectedRow();
            if (row >= 0) {
                String id = (String) tblKH.getValueAt(row, 0);
                KhachHangDao dao = new KhachHangDao();
                KhachHang n = dao.FindByID(id);

                if (n != null) {
                    txtMAKH.setText(n.getMaKH());
                    txtHOTEN.setText(n.getHoTen());
                    txtDCHI.setText(n.getDChi());
                    txtSODT.setText(n.getSdt());
                    txtNGSINH.setText(n.getNgSinh());

                    txtDOANHSO.setText(String.valueOf(n.getDoanhSo()));
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.toString(), "Lỗi");
        }
    }//GEN-LAST:event_tblKHMouseClicked

