    private void btnTimKHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKHActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtTimMAKH, sb, "Mã khách hàng không được để trống.");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        try {
            KhachHangDao dao = new KhachHangDao();
            KhachHang n = new KhachHang();
            n = dao.FindByID(txtTimMAKH.getText());
            if (n != null) {
                tblModel.setRowCount(0);
                tblModel.addRow(new Object[]{
                    n.getMaKH(), n.getHoTen(), n.getDChi(), n.getSdt(), n.getNgSinh(),
                    n.getNgDK(), n.getDoanhSo()});
                tblModel.fireTableDataChanged();
                txtTimMAKH.setText("");
            } else {
                MessageDialogHelper.showErrorDialog(parentForm, "Không tìm thấy!", "Thông báo!");
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi!");
        }
    }//GEN-LAST:event_btnTimKHActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMAKH, sb, "Mã khách hàng không được để trống.");
        DataValidator.validateEmpty(txtHOTEN, sb, "Họ tên khách hàng không được để trống.");
        DataValidator.validateEmpty(txtSODT, sb, "Số điện thoại không được để trống.");
        DataValidator.validateEmpty(txtNGDK, sb, "Ngày đăng ký không được để trống.");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }

        KhachHang n = new KhachHang();
        n.setMaKH(txtMAKH.getText());
        n.setHoTen(txtHOTEN.getText());
        n.setDChi(txtDCHI.getText());
        n.setSdt(txtSODT.getText());
        n.setNgSinh(txtNGSINH.getText());
        n.setNgDK(txtNGDK.getText());
        n.setDoanhSo(Long.parseLong(txtDOANHSO.getText()));

        if (new KhachHangDao().Insert(n)) {
            MessageDialogHelper.showMessageDialog(parentForm, "Khách hàng đã được thêm!", "Thông báo!");
            loadData();
            //list.add(n); // them vao danh sach KH
        } else {
            MessageDialogHelper.showErrorDialog(parentForm, "Thêm không thành công!", "Thông báo!");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMAKH, sb, "Mã khách hàng không được để trống.");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }

        if (MessageDialogHelper.showConfirmDialog(parentForm, "Bạn có muốn xoá khách hàng không?",
                "Hỏi") == JOptionPane.YES_OPTION) {
            KhachHangDao dao = new KhachHangDao();
            if (dao.Delete(txtMAKH.getText())) {
                MessageDialogHelper.showMessageDialog(parentForm, "Khách hàng đã được xóa!", "Thông báo!");
                loadData();
                btnLamMoiActionPerformed(evt);
            } else {
                MessageDialogHelper.showMessageDialog(parentForm, "Xóa không thành công do lỗi!", "Thông báo!");
            }
        }

    }//GEN-LAST:event_btnXoaActionPerformed

