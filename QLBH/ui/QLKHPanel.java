    private void initTable() {
        tblModel = new DefaultTableModel();
        tblModel.setColumnIdentifiers(new String[]{"MÃ KHÁCH HÀNG", "HỌ TÊN", "ĐỊA CHỈ", "SỐ ĐIỆN THOẠI",
            "NGÀY SINH", "NGÀY ĐĂNG KÝ", "DOANH SỐ"});
        tblKH.setModel(tblModel);
    }

    private void loadData() {
        try {
            KhachHangDao dao = new KhachHangDao();
            List<KhachHang> list = dao.FindAll();
            tblModel.setRowCount(0);
            for (KhachHang n : list) {
                tblModel.addRow(new Object[]{
                    n.getMaKH(), n.getHoTen(), n.getDChi(), n.getSdt(),
                    n.getNgSinh(), n.getNgDK(), n.getDoanhSo()});
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi!");
        }

    }

